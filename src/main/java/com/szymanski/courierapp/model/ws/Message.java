public class Message {

  private String text;

  private String receiver;

  public String getReceiver() {
    return receiver;
  }

  public void setReceiver(String receiver) {
    this.receiver = receiver;
  }

  public String getText() {
    return text;
  }

  public void setText(String text) {
    this.text = text;
  }



}
